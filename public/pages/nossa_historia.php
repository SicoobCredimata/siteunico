        <span class="titleCredimataCooperativismo">Cooperativismo</span>
        <span class="textCredimataCooperativismo">
            <b>Cooperativismo significa a colaboração entre pessoas com um interesse em comum.</b> A ideia surgiu após a Revolução Industrial, quando a população começou a ficar insatisfeita com as altas taxas de desemprego e os baixos salários. O cenário motivou os trabalhadores a se unirem em busca de melhores condições para o exercício das suas atividades, que incluíam desde a redução nos custos de produção até a aquisição de novos equipamentos. <br><br>
            No entanto, foi apenas em 1844 que o <b>termo cooperativa</b> surgiu. A primeira cooperativa foi criada na cidade de Rochdale-Manchester, no interior da Inglaterra. <br><br>
            Um grupo composto por 27 homems e uma mulher se uniu para montar seu próprio armazém, já que, separadamente, essas pessoas não tinham condições de comprar a quantidade básica de alimentos para sobreviver. <br><br>
            A proposta era simples: comprar alimentos em grande quantidade para conseguir preços menores. Todos os itens seriam divididos igualitariamente entre o grupo. E assim nasceu a <b>Sociedade dos Probos de Rochdale</b>, que em 4 anos multiplicou seu capital social de 28 para 152 mil libras. <br><br>
            O termo cooperativismo, contudo, só foi criado oficialmente em 1948, em um congresso do segmento realizado em Praga. Na época, sua <b>definição</b> era: “Será considerada como cooperativa, seja qual for a constituição legal, toda a associação de pessoas que tenha por fim a melhoria econômica e social de seus membros pela exploração de uma empresa baseada na ajuda mínima e que observa os Princípios de Rochdale”. <br><br>
            Desde então, já são mais de 3 milhões de cooperativas no mundo. Existe até o <b>Dia Internacional do Cooperativismo</b>, comemorado no primeiro sábado de julho e com objetivo de divulgar esse modelo organizacional.
        </span>
