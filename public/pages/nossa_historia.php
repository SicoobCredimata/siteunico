        <span class="titleCredimata">O Sicoob Credimata</span>
        <span class="textCredimata">Iniciou em setembro de 2008, em uma Reunião Ordinária da UAR 2 realizada em Leopoldina em que foi levantado em plenário o assunto sobre aglutinação de cooperativas de crédito. </span>
        <span class="textCredimata">Tema este já em outros ambientes inerentes ao segmento muito debatido. Naquela oportunidade se ensaiou sobre a possibilidade desse acontecimento entre as cooperativas de crédito de Além Paraíba, Leopoldina, São João Nepomuceno e Volta Grande. </span>
        <span class="textCredimata">Terminada a reunião, entre os administradores esta ideia não ficou latente. Passaram alguns dias e logo se originou movimentos, conversas e reuniões. Muito se discutiu buscando a viabilidade do projeto, sob orientação e condução do SICOOB CENTRAL CREDIMINAS. Passado este tempo de estudos a aglutinação se materializou em 30 de abril de 2009. Consolidado o processo, dirigentes, colaboradores e associados entraram num processo de transição. Conhecendo novas realidades buscando adaptar-se ao novo. </span>
        <span class="textCredimata">No contexto da época prevaleceram instâncias denominadas de Unidade Regional num total de quatro. Estas regionais representavam as áreas de atuação de cada uma das cooperativas aglutinadas. Cada regional tinha sua representatividade na Diretoria e Conselho de Administração. </span>
        <span class="textCredimata">Foi implementado à época um Centro Administrativo em Volta Grande, município sede da cooperativa incorporadora – já possuía a “Livre Admissão”, com apoio à Contabilidade, Compensação, Departamento Pessoal, dentre outros, permanecendo o setor de crédito nas ex-sedes – das cooperativas incorporadas.</span>
        <span class="textCredimata">Buscando e primando melhoria nos processos, em fevereiro de 2014, fez-se a centralização do setor de crédito em Leopoldina, oportunidade de ganho de negócios e celeridade nas liberações. </span>
        <span class="textCredimata">Além de sua sede, a Cooperativa possui ainda 22 (vinte e duas) agências, localizadas nos municípios mineiros de: Além Paraíba, Bicas, Cataguases, Descoberto, Itamarati de Minas, Juiz de Fora - Benfica, Juiz de Fora - Centro, Juiz de Fora – Moinho, Laranjal, Leopoldina, Mar de Espanha, Maripá de Minas, Palma, Pirapetinga, Recreio, Rio Novo, Rochedo de Minas, Santo Antônio do Aventureiro, São João Nepomuceno, Senador Cortes e no município de Sapucaia e Teresópolis no Rio de Janeiro.</span>
